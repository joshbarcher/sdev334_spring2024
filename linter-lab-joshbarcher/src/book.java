public class book {
    String title, author;

    book(String Title, String Author) {
        this.title = Title;
        this.author = Author;
    }

    public String getTitle() {
        return title;
    }
    public void setTitle(String title) {
        this.title = title;
    }
    public String getAuthor() {
        return author;
    }
    public void setAuthor(String author) {
        this.author = author;
    }
}
